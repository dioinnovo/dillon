export function getStellaLeadsQuickAction(action: keyof typeof STELLA_LEADS_QUICK_ACTIONS, context?: Record<string, string>): string {
  let prompt = STELLA_LEADS_QUICK_ACTIONS[action]

  if (context) {
    Object.entries(context).forEach(([key, value]) => {
      prompt = prompt.replace(`{${key}}`, value)
    })
  }

  return prompt
}